#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	s_length;
	size_t	i;

	s_length = ft_strlen(s);
	if (!s)
		return (0);
	if (s_length < start)
		return (ft_strdup(""));
	if (s_length < start + len)
		len = s_length - start;
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (0);
	i = 0;
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
